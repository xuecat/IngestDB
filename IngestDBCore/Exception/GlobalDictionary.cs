        /// <returns>错误信息</returns>
        public string GetMessageByCode(int nCode)
        {
            string strMessage = "";

            if (_globalDict.TryGetValue(nCode, out strMessage))
            {
                strMessage = Locallanguage.LoadString(strMessage);
            }

            return strMessage;
        }

