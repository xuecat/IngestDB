        /// <summary>
        /// 单件实体
        /// </summary>
        public static GlobalDictionary Instance
        {
            get
            {
                if (_instance == null)
                {
                    lock (_globalDicLock)
                    {
                        if (_instance == null)
                        {
                            _instance = new GlobalDictionary();
                        }
                    }
                }

                return _instance;
            }
        }

